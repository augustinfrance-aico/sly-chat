"""
R&D Lab — LABRAT (Experimental Lab AI)
Prototypage experimental : paper → spec → code → test → document.
Pipeline click-to-prototype. Zero cout.
"""

import json
import logging
from datetime import datetime
from pathlib import Path

from ..config import MEMORY_DIR
from ..ai_client import chat as ai_chat

log = logging.getLogger("titan.rdlab.experiment")

EXPERIMENTS_FILE = MEMORY_DIR / "rdlab_experiments.json"
PAPERS_FILE = MEMORY_DIR / "rdlab_papers.json"
EXPERIMENTS_DIR = Path(__file__).parent.parent.parent.parent / "experiments"


class TitanRDLabExperiment:
    """LABRAT — Le Laborantin. Paper → prototype en un clic."""

    def __init__(self):
        self.data = self._load()
        EXPERIMENTS_DIR.mkdir(exist_ok=True)

    # === PERSISTENCE ===

    def _load(self) -> dict:
        if EXPERIMENTS_FILE.exists():
            try:
                with open(EXPERIMENTS_FILE, "r", encoding="utf-8") as f:
                    return json.load(f)
            except (json.JSONDecodeError, OSError):
                pass
        return {"experiments": [], "stats": {"proposed": 0, "generated": 0, "tested": 0, "adopted": 0, "rejected": 0}}

    def _save(self):
        with open(EXPERIMENTS_FILE, "w", encoding="utf-8") as f:
            json.dump(self.data, f, ensure_ascii=False, indent=2)

    def _load_papers(self) -> list:
        """Load papers from ARXIV digestor cache."""
        if PAPERS_FILE.exists():
            try:
                with open(PAPERS_FILE, "r", encoding="utf-8") as f:
                    return json.load(f).get("papers", [])
            except (json.JSONDecodeError, OSError):
                pass
        return []

    # === SPEC GENERATION ===

    def generate_spec(self, paper: dict) -> str:
        """Generate a prototype specification from a paper."""
        prompt = f"""Paper: {paper.get('title', '')}
Abstract: {paper.get('abstract', '')[:400]}

Genere une spec de prototype minimal Python:
1. Ce que ca fait (1 phrase)
2. Dependencies (pip-installable uniquement, gratuites)
3. Lignes de code estimees (<100)
4. Input attendu / Output attendu
5. Comment ca se compare au stack AICO (agents autonomes, Groq/Gemini, zero-cost)
Format structure, ultra-concis."""

        return ai_chat(
            "Tu es LABRAT, expert prototypage rapide. Specs minimales, executables.",
            prompt,
            max_tokens=400,
        )

    def generate_code(self, spec: str, paper_title: str) -> str:
        """Generate experimental Python code from a spec."""
        prompt = f"""Spec:
{spec}

Paper: {paper_title}

Genere un script Python minimal et fonctionnel:
- <100 lignes
- Dependencies pip-installable uniquement (pas de GPU requis)
- Standalone (executable directement)
- Avec un if __name__ == "__main__": pour tester
- Commentaires en francais
- IMPORTANT: code FONCTIONNEL, pas pseudo-code"""

        return ai_chat(
            "Tu es LABRAT, expert code experimental Python. Code propre, minimal, fonctionnel.",
            prompt,
            max_tokens=800,
        )

    # === CLICK-TO-PROTOTYPE ===

    async def click_to_prototype(self, paper_id: str) -> str:
        """Full pipeline: paper → spec → code → save → log."""
        # 1. Find paper
        papers = self._load_papers()
        paper = next((p for p in papers if p.get("id") == paper_id), None)
        if not paper:
            return f"🧪 Paper '{paper_id}' non trouve. Utilise /rdlab papers pour voir les IDs."

        title = paper.get("title", "unknown")
        log.info(f"Click-to-prototype: {title}")

        # 2. Generate spec
        spec = self.generate_spec(paper)

        # 3. Generate code
        code = self.generate_code(spec, title)

        # Extract Python code block if wrapped in markdown
        if "```python" in code:
            start = code.index("```python") + 9
            end = code.index("```", start)
            code = code[start:end].strip()
        elif "```" in code:
            start = code.index("```") + 3
            end = code.index("```", start)
            code = code[start:end].strip()

        # 4. Save to experiments/
        safe_name = "".join(c if c.isalnum() or c in "_-" else "_" for c in paper_id)
        filepath = EXPERIMENTS_DIR / f"{safe_name}_prototype.py"
        header = f'"""\nExperiment: {title}\nGenerated by LABRAT — R&D Lab\nDate: {datetime.now().isoformat()}\nPaper: {paper.get("url", "")}\n"""\n\n'
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(header + code)

        # 5. Log experiment
        experiment = {
            "id": paper_id,
            "title": title,
            "paper_url": paper.get("url", ""),
            "spec": spec[:500],
            "code_file": str(filepath),
            "status": "generated",
            "created": datetime.now().isoformat(),
            "result": None,
        }
        self.data["experiments"].append(experiment)
        self.data["stats"]["generated"] = self.data["stats"].get("generated", 0) + 1
        # Keep max 50 experiments
        if len(self.data["experiments"]) > 50:
            self.data["experiments"] = self.data["experiments"][-50:]
        self._save()

        return (
            f"🧪 PROTOTYPE GENERE\n\n"
            f"📄 Paper: {title}\n"
            f"📁 Fichier: {filepath}\n"
            f"📋 Spec:\n{spec[:300]}\n\n"
            f"⚠️ Le code n'est PAS auto-execute. Review et lance manuellement."
        )

    # === LIST & STATUS ===

    def list_experiments(self, status: str = None) -> str:
        """List experiments, optionally filtered by status."""
        experiments = self.data.get("experiments", [])
        if status:
            experiments = [e for e in experiments if e.get("status") == status]

        if not experiments:
            return f"🧪 Aucune experience{f' ({status})' if status else ''}."

        output = f"🧪 EXPERIENCES — {len(experiments)} total\n\n"
        for e in experiments[-10:]:  # Last 10
            status_emoji = {
                "proposed": "📝",
                "generated": "⚙️",
                "tested": "🔬",
                "adopted": "✅",
                "rejected": "❌",
            }.get(e.get("status", ""), "❓")
            output += f"{status_emoji} [{e['status']}] {e['title'][:60]}\n"
            output += f"   ID: {e['id']} | {e['created'][:10]}\n\n"
        return output

    def update_status(self, paper_id: str, new_status: str, result: str = None) -> str:
        """Update experiment status (tested/adopted/rejected)."""
        for exp in self.data.get("experiments", []):
            if exp.get("id") == paper_id:
                old_status = exp["status"]
                exp["status"] = new_status
                if result:
                    exp["result"] = result
                exp["updated"] = datetime.now().isoformat()

                # Update stats
                self.data["stats"][new_status] = self.data["stats"].get(new_status, 0) + 1
                self._save()

                return f"🧪 {exp['title'][:50]} : {old_status} → {new_status}"

        return f"🧪 Experiment '{paper_id}' non trouve."

    # === COMMAND HANDLER ===

    def handle_command(self, text: str) -> str:
        """Handle /rdlab experiment commands."""
        parts = text.strip().split(maxsplit=4)

        if len(parts) >= 3 and parts[2] == "list":
            status = parts[3] if len(parts) >= 4 else None
            return self.list_experiments(status)

        if len(parts) >= 4 and parts[2] == "proto":
            paper_id = parts[3]
            import asyncio
            return asyncio.get_event_loop().run_until_complete(self.click_to_prototype(paper_id))

        if len(parts) >= 5 and parts[2] == "status":
            paper_id = parts[3]
            new_status = parts[4]
            return self.update_status(paper_id, new_status)

        # Default: list all
        return self.list_experiments()
